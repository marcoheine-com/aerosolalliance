import type { GetStaticProps, NextPage } from 'next'
import Head from 'next/head'
import Client from '../prismicHelpers'
// @ts-ignore
import SliceZone from 'next-slicezone'
import * as Slices from '../slices'
import { Footer } from '../components/footer'

// @ts-ignore
const resolver = ({ sliceName }) => Slices[sliceName]

export const getStaticProps: GetStaticProps = async () => {
  const home = (await Client().getSingle('home', {})) || null
  const footer = (await Client().getSingle('footer', {})) || null

  return {
    props: {
      home,
      footer,
    },
  }
}

interface Props {
  home: any
  footer: any
}

const Home: NextPage<Props> = (props) => {
  return (
    <>
      <Head>
        <title>Aerosol Aliance</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <main className="max-w-[1920px] flex flex-col mx-auto">
        <SliceZone slices={props.home.data.slices} resolver={resolver} />
        <Footer data={props.footer.data} />
      </main>
    </>
  )
}

export default Home
